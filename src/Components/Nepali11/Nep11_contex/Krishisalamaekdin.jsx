      <div>
        <h5>८. समीक्षात्मक उत्तर दिनुहोस :</h5>
        <h5>(क) दिगो कृषि पद्धतिका लागि के कस्ता कुरामा ध्यान पुर्‍याउनु पर्छ ?</h5>
        <p>= नेपाल कृषि प्रधान देश हो र यहाँको अधिकांश जनसंख्या प्रत्यक्ष या अप्रत्यक्ष रूपमा कृषिमा निर्भर छ। तर विडम्बना, यहाँ कृषिकर्मलाई अझै पनि परम्परागत, श्रमप्रधान र कम उत्पादनशील पेसाको रूपमा लिइन्छ। ‘कृषिशालामा एक दिन’ नामक पाठमा कृषि क्षेत्रको वर्तमान अवस्था, समस्याहरू र त्यसको दिगो समाधानका उपायहरूबारे गम्भीर विश्लेषण गरिएको छ। यो पाठले कृषिको दीर्घकालीन विकासका लागि नीति, प्रविधि, समाज र सरकारले ध्यान दिनुपर्ने मुख्य पक्षहरू प्रस्तुत गरेको छ।</p>
        <p>दिगो कृषि भन्नाले त्यस्तो कृषि प्रणाली बुझिन्छ, जसले उत्पादनको निरन्तरता मात्र नभई पर्यावरणीय संरक्षण, सामाजिक न्याय र आर्थिक समृद्धिलाई समेत सुनिश्चित गर्छ। दिगो कृषिका लागि सबैभन्दा पहिले सरकारले भू–प्रकृति, जलवायु, र जैविक विविधताको संरक्षणलाई प्राथमिकता दिनु अत्यावश्यक छ। विभिन्न भौगोलिक क्षेत्रअनुसार फरक–फरक खेती प्रणाली अपनाउने नीति ल्याउनुपर्छ। एकै प्रकारको नीति सबै क्षेत्रमा लागू गर्नु उपयुक्त हुँदैन।दिगो कृषि पद्धतिको अर्को आधार हो, परम्परागत ज्ञान र अनुभवको संरक्षण तथा उपयोग। नेपाली कृषकले पुस्तौंदेखि गर्दै आएको खेतीपातीका परम्परागत तरिकामा थुप्रै ज्ञान र सीपहरू छन्, जसलाई वैज्ञानिक अध्ययनद्वारा सुधार र आधुनिकीकरण गरेर प्रयोगमा ल्याउन सकिन्छ। त्यसका लागि अनुसन्धान केन्द्र, कृषि विश्वविद्यालय र विज्ञ व्यक्तिहरूको सक्रिय भूमिका हुनुपर्छ।</p>
        <p>यसबाहेक, कृषिमा नयाँ प्रविधिको प्रयोग दिगोपनको अर्को महत्वपूर्ण पक्ष हो। जैविक मल, सिँचाइको वैज्ञानिक प्रणाली, रोग प्रतिरोधात्मक बिउ, र आधुनिक उपकरणको प्रयोगले उत्पादन वृद्धिसँगै माटो र जल स्रोतको संरक्षण पनि गर्न सकिन्छ। रासायनिक मलको अत्यधिक प्रयोगले जमिनको उर्वरता घटाउने भएकाले वैकल्पिक उपायहरू अवलम्बन गर्नुपर्छ।दिगो कृषि प्रणालीको सफल कार्यान्वयनको लागि मानव संसाधन विकास पनि अत्यन्तै आवश्यक छ। त्यसका लागि युवापुस्तालाई कृषितर्फ आकर्षित गर्नुपर्ने आवश्यकता छ। आजका धेरै युवा विदेश पलायन भइरहेका छन्, किनकि उनीहरूले कृषिमा भविष्य देख्दैनन्। कृषिमा सम्मान, आधुनिकता, प्रविधि र प्रतिफल देखिए मात्र उनीहरू यस पेसातर्फ आकर्षित हुन्छन्। त्यसैले, कृषि क्षेत्रमा स्वरोजगार, स्टार्टअप, उद्यमशिलता, सहकारी प्रणाली र व्यावसायिक तालिमको विकास गर्नुपर्छ।</p>
        <p>साथै, कृषकको जीवनस्तर उठाउनका लागि बीमा, अनुदान, कर्जाको सहज पहुँच, बजार व्यवस्थापन, मूल्य स्थिरता र सरकारी संरक्षणको व्यवस्था अनिवार्य छ। सरकारले नीति मात्र बनाउनु होइन, तिनको प्रभावकारी कार्यान्वयन सुनिश्चित गर्नु जरुरी छ। बजारमा हुने विचौलियाको नियन्त्रण, किसानको सिधा पहुँच बजारमा कायम गरिनुपर्छ। दिगो कृषिका लागि सबै पक्षको सहभागिता आवश्यक छ। नीति निर्माता, वैज्ञानिक, कृषि अभियन्ता, स्थानीय सरकार, र कृषकहरू सबैले आ–आफ्नो भूमिकामा सजग, जिम्मेवार र सकारात्मक रहनुपर्छ। एक जना वा एउटै संस्था मात्रैले दिगो कृषि सम्भव बनाउँदैन, बरु यो साझा प्रयासद्वारा मात्र सम्भव हुन्छ।</p>
        <p>अन्ततः, ‘कृषिशालामा एक दिन’ नामक पाठले कृषिलाई समृद्ध र दिगो पेसा बनाउन हामीले गर्ने प्रयास, सोच, नीति र कार्यान्वयनको स्तर कस्तो हुनुपर्छ भन्ने कुरा उजागर गरेको छ। कृषिमा भविष्य देख्ने सोच र सोचलाई कार्यान्वयनमा रूपान्तरण गर्ने संकल्प भए मात्र दिगो कृषि सम्भव हुन्छ।  </p>
      </div>
