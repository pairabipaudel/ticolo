      <div>
        <h5>(ख) "वातावरण प्रदुषणको मूल कारण मान्छे नै भएकाले यसको समाधान पनि मानिसले नै गर्नुपर्छ ।" संवादांत्मक सन्दर्भ समेत ख्याल गरी आफ्ना तर्कहरु प्रस्तुत गर्नुहोस् ।</h5>
        <p>= वातावरण प्रदूषण भन्नाले प्राकृतिक तत्त्वहरू जस्तै हावा, पानी, माटो, ध्वनि, ताप आदिमा हुने अस्वाभाविक तथा हानिकारक परिवर्तनलाई जनाइन्छ। आज विश्वभर वातावरणीय सन्तुलनमा गम्भीर समस्या उत्पन्न भएको छ, जसको प्रमुख कारण मानवीय क्रियाकलाप हुन्। अन्धाधुन्ध वन फडानी, औद्योगिक फोहोर, सवारी साधनको धुँवा, प्लास्टिकको अत्यधिक प्रयोग, र रासायनिक मल तथा कीटनाशकको अत्यधिक प्रयोग जस्ता मानवीय गतिविधिले आजको पृथ्वीलाई प्रदूषित बनाइरहेको छ। मानिसले नै प्रकृतिको स्वरूपमा हस्तक्षेप गरेपछि अहिले हामी जलवायु परिवर्तन, जैविक सन्तुलनको विघटन, ओजन तहको पातलोपन र अत्यधिक तापमान वृद्धिको सामना गरिरहेका छौँ।</p>
        <p>वातावरण प्रदूषणको जरो नै मानव भएकोले यसको समाधान पनि मानिसबाटै सम्भव छ। समाधानका लागि सबैभन्दा पहिले मानिसको सोच, प्रवृत्ति र जीवनशैलीमा परिवर्तन ल्याउनु आवश्यक छ। एकातिर आधुनिक प्रविधि प्रयोग गर्दा अर्कोतिर त्यसको असर सोच्न जरुरी छ। वृक्षरोपण, जैविक कृषि प्रवर्द्धन, जल स्रोतको संरक्षण, फोहोर व्यवस्थापन, नवीकरणीय ऊर्जा प्रयोग जस्ता उपायलाई आत्मसात् गर्नुपर्छ। साथै, दिगो विकासको अवधारणा अनुरूप वातावरणमैत्री योजना र नीति अपनाई कार्यान्वयन गर्न आवश्यक छ। विद्यालयदेखि समाजसम्म जनचेतना फैलाउनु, वातावरणीय शिक्षा दिनु र सामूहिक पहलमार्फत प्रदूषण घटाउनु आजको आवश्यकता हो।</p>
        <p>यसरी हेर्दा वातावरण प्रदूषण कुनै स्वतःस्फूर्त समस्या होइन, यो मानवले सिर्जना गरेको गम्भीर समस्या हो। तसर्थ, यसको समाधानको दायित्व पनि मानव आफैँले लिनुपर्छ। हामीले प्रकृति प्रतिको जिम्मेवारी बुझेमा मात्र भविष्य सुरक्षित हुनेछ। समयमै सचेत भइ आजैबाट हरित अभियानमा लागेमा स्वच्छ, स्वस्थ र समुन्नत जीवन सम्भव छ। वातावरण जोगाउनु भनेको आफैँलाई बचाउनु हो भन्ने चेत सबैमा फैलाउनु अत्यन्तै जरुरी छ।</p>
      </div>
