       <div>
        <h5>७. ममताले सुबोधलाई परित्याग गरेर छोराछोरीसँग नै सम्पूर्ण जीवन बिताउने भनी गरेको निर्णय तपाईंलाई कस्तो लाग्यो, कथाका आधारमा विश्लेषण गर्नुहोस् ।</h5>
        <p>= ममताले सुबोधलाई परित्याग गरेर छोराछोरीसँगै जीवन बिताउने निर्णय कथाका आधारमा निकै उपयुक्त र सार्थक छ। ‘मातृत्व’ कथामा ममताको पात्रले मातृत्वशक्ति र नारीको मानसिक द्वन्द्वलाई सूक्ष्मतापूर्वक प्रस्तुत गरिएको छ। पति गुमाएपछि ममता नीरस र अधुरो जीवन बिताइरहेकी हुन्छिन्। त्यस क्रममा सुबोधले उनलाई मानसिक सुरक्षा र माया दिने आशा लिएर आएका थिए, तर उनको कामुक र स्वार्थी व्यवहारले ममताको पीडा झन् बढाइदियो।</p>
        <p> </p>
        <p>सुबोधले ममताका छोराछोरीलाई मन नपराउनु, उनीहरूप्रति ईर्ष्या गर्नु, र ममताप्रति अनुचित व्यवहार गर्नुले ममता दुखी भइन्। जब उनकी छोरी दुर्घटनामा परेपछि सुबोधले देखाएको बेवास्ता र असहयोगले ममतामा आफ्ना सन्तानप्रति मातृत्व प्रेम अझ प्रबल भयो। यसले उनलाई सुबोधसँगको सम्बन्ध तोडेर आफ्ना छोराछोरीसँगै मात्र बस्न प्रेरित गर्‍यो।</p>
        <p> </p>
        <p>ममताको निर्णयले आमाले आफ्ना छोराछोरीका लागि गर्ने बलिदान र त्यागलाई प्रमाणित गर्छ। जीवनका असफल र पीडादायी सम्बन्धलाई छोडेर मातृत्वको प्रेमलाई सर्वोपरि राख्नु आमाको सच्चा मर्म हो। सुबोधजस्तो स्वार्थी र कमजोर व्यक्ति भन्दा आफ्ना सन्तानको भलो र सुखलाई प्राथमिकता दिनु नै ममताको सही निर्णय थियो।</p>
        <p></p>
        <p>यसरी ममताले गरेको निर्णय सामाजिक र नैतिक दुवै दृष्टिले प्रशंसनीय छ। यसले नारीहरूको मानसिक संघर्ष, मातृत्व प्रेम र जीवनका असारह निर्णयहरूको प्रतिनिधित्व गर्दछ। अतः ममताको निर्णयलाई मैले उचित, साहसी र गम्भीर सोचको उपज भएको मान्दछु।</p>
       </div>
