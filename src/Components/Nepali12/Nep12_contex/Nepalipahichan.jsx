        <p>= म बिहान सबेरै ६ बजे उठिन्छ। दाँत माझिएर हातमुख धोइन्छ र नित्यकर्म पूरा गरिन्छ। त्यसपछि चिया पिइन्छ र विद्यालय जानको तयारी गरिन्छ। समयमै खाजा खाइन्छ र पोशाक लगाइन्छ। विद्यालयमा पढाइ ध्यानपूर्वक गरिन्छ र शिक्षकद्वारा दिइएका कार्यहरू पूरा गरिन्छ। बेलुका घर फर्किएपछि होमवर्क गरिन्छ र केही समय टेलिभिजन हेर्ने वा पुस्तक पढ्ने गरिन्छ। राति खाना खाएपछि समयमै सुतिन्छ। यसरी मेरो दैनिक दिनचर्या कर्तृवाच्यको प्रयोग गरी पूरा गरिन्छ।</p>
