        <p>छिरिङ हरेक दिन नियमित रूपमा कुनै न कुनै नयाँ पुस्तक पढ्ने गर्थे । उनले कथा, कविता, उपन्यास र यात्रा सस्मरण विधाका धेरै किताब पढिसकेका थिए । ‘मुनामदन’ पढ्दा उनी निकै भावुक बनेका थिए । पढ्दाखेरि केही कुरा नबुझ्दा उनी हजुरआमासँग जान्थे र किताब देखाउँदै भन्थे, “हजुरआमा यसको अर्थ के हो ?” किताब देख्नेबित्तिकै हजुरआमा भन्नुहुन्थ्यो, “ओहो ! तिमीले यो के ल्यायौ ?” हजुरआमालाई भने काला अक्षर भैंसी बराबर थिए । यसैले हजुरआमाले सम्झाएको भावमा उनलाई भन्नुहुन्थ्यो, “मैले तिमीहरूको जस्तो बेलामा विद्यालय जान पाइनँ । यसैले मलाई केही आउँदैन । तिमीहरू त धेरै भाग्यमानी छौ । सानैदेखि विद्यालय जान पाएका छौ आमाबुबासित सोध्न पाएका छौ त्यसैले भरे आमा आएपछि सोध न ।” यसो भने पनि उनी हजुरआमाका कुरा बारम्बार सुनिरहन्थे । आगो ताप्नु मुढाको कुरा सुन्नु बुढाको भने झैँ छिरिङ हजुरआमाको अनुभवपूर्ण कुराबाट प्रत्येक दिन केही न केही नयाँ कुरा सिकिरहेका हुन्थे ।</p>
