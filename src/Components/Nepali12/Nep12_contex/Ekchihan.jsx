      <div>
        <h5>१४. दिइएको अनुच्छेद पढी सोधिएका प्रश्नको उत्तर लेख्नुहोस् :</h5>
        <h5>(क) “उस्तरी बस्ती थिएन” भन्नुको तात्पर्य के हो ?</h5>
        <p>= “उस्तरी बस्ती थिएन” भन्नुको तात्पर्य हो त्यो ठाउँमा मानिसहरूको बसोबास कम मात्रामा वा विरलै भएको थियो अर्थात् त्यहाँ घरहरू घना रूपमा थिएनन् र मानिसहरू थोरै मात्र बस्दै आएका थिए।</p>
        <h5>(ख) सम्साँझमै स्याल कराउनका लागि कुन कुराले सहयोग गरेको थियो ?</h5>
        <p>= साँझमै स्याल कराउनका लागि तितेपातीका घारी र खरीका रुखले सहयोग गरेको थियो।</p>
        <h5>(ग) कसरी कोटेश्वर आधुनिक जङ्गलमा बदलियो ?</h5>
        <p>= मानिसका घारी, आधुनिक भवनहरू, बिजुलीका पोल र तारका कारणले कोटेश्वर आधुनिक जङ्गलमा बदलियो ।</p>
        <h5>(घ) अहिलेको कोटेश्वरमा कस्ता मानिस बस्छन् ?</h5>
        <p>= अहिलेको कोटेश्वरमा नेता, कार्यकर्ता, व्यापारी, सुरक्षाकर्मी, न्यायकर्ता, साहु–महाजन, उद्योगी, चोर–फटाहा सबै किसिमका मानिसहरू बसोबास गर्छन्।</p>
      </div>
